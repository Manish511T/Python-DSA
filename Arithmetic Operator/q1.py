
# For a given number int n=5783;
# a. Print the last digit of the number
# b. Print the last two digit of the number
# c. Remove the last digit of the number
# d. Remove the last two digit of the number

n=5783

# a. Print the last digit of the number
print(n%10)

# b. Print the last two digit of the number
print(n%100)

# c. Remove the last digit of the number
print(n//10)

# d. Remove the last two digit of the number
print(n//100)
